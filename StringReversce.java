
public class StringReversce {

    public static void main(String[] args) {
        
        String fullName = "MuhammadYousufAli";
        StringBuffer sbr = new StringBuffer(fullName);
        System.out.println(sbr.reverse());
        String myCell = "01723811717";
        sbr = new StringBuffer(myCell);
        System.out.println(sbr.reverse());
        
    }
    
}
